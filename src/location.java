
public class location 
{
	private String name;
	private int location;
	
	
	// Constructor
	public location (String name, int location)
	{
		this.name = name;
		this.location = location;
	}
	
	public String getName()
	{
		return this.name;
	}
	public int getLocation()
	{
		return this.location;
	}

}
